package bibliothek;

/**
 * represents a point in 2d
 * @author Alexander Buyanov (806984)
 * @author Phillip Redlich (791806)
 */
public class Point2 {

    /**
     * Coordinate x of the point
     */
    public double x;

    /**
     * Coordinate y of the point
     */
    public double y;

    public Point2(double x, double y) {
        this.x = x;
        this.y = y;
    }

}
